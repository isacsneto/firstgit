    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String a1 = txtValorr.getText();
        String a1c = a1.replaceAll(",", ".");
        double v1 = Double.parseDouble(a1c);
        double vt = VendasProdutosDAO.getValorTotalProductID();
        if(ChecarCpf.checkCpfCliente(txtCpf.getText()) && v1 >= vt) { // debugar
            // possivel problema no txtValorr
            Venda v = new Venda();
            VendasDAO vdao = new VendasDAO();
            
            v.setClientev(txtCpf.getText());
            v.setIdvenda(VendasProdutosDAO.getAutoIncrementProductID());
            vdao.update2(v);  
            
            JOptionPane.showMessageDialog(null, "Venda finalizada com sucesso!");
            this.dispose();
            
        } else {
            JOptionPane.showMessageDialog(null, "CPF inv√°lido ou Valor recebido abaixo do esperado.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

