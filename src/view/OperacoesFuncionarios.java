    private void btnAlterarFunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarFunActionPerformed
        
         if(jTFuncionarios.getSelectedRow() != -1 && ChecarCpf.checkCpfUsuario(txtCPFFun.getText())){
            
            FuncionariosDAO fdao = new FuncionariosDAO();
            Funcionario f1 = new Funcionario();
            
            f1.setNome(txtNomeFun.getText());
            f1.setCpf((txtCPFFun.getText()));
            f1.setEmail(txtEmailFun.getText());
            f1.setData_nasc(txtDataFun.getText());
            f1.setCelular((txtCelularFun.getText()));
            f1.setCep((txtCEPFun.getText()));
            f1.setRua((txtRuaFun.getText()));
            f1.setNumero((txtNumeroFun.getText()));
            f1.setFuncao(choice1.getSelectedItem());
            
            fdao.update(f1);
            
            txtNomeFun.setText("");
            txtCPFFun.setText("");
            txtEmailFun.setText("");
            txtDataFun.setText("");
            txtCelularFun.setText("");
            txtCEPFun.setText("");
            txtRuaFun.setText("");
            txtNumeroFun.setText("");
            
            
            readJTable();
            
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um registro!");
        }
    }//GEN-LAST:event_btnAlterarFunActionPerformed

