     public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTFuncionarios.getModel();
        modelo.setNumRows(0);
        
        FuncionariosDAO pdao = new FuncionariosDAO();

        for(Funcionario f: pdao.read()){
            
            modelo.addRow(new Object[]{
                f.getNome(),
                f.getCpf(),
                f.getEmail(),
                f.getData_nasc(),
                f.getCelular(),
                f.getCep(),
                f.getRua(),
                f.getNumero(),
                f.getFuncao()
            });
    
    }
 }
