    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        PreparedStatement ps;
        ResultSet rs;
        String ucpf = jFormattedTextField1.getText();
        String pass = String.valueOf(jPasswordField1.getPassword());
        String ufunc = "Supervisor";
        
        String query = "SELECT * FROM `funcionario` WHERE `cpf` =? AND `senha` =?";
        
        try {
            ps = ConnectionFactory.getConnection().prepareStatement(query);
            
            ps.setString(1, ucpf);
            ps.setString(2, pass);

            
            rs = ps.executeQuery();
            
            if(rs.next())
            {
                if(ChecarFunc.checkFunc(ufunc,ucpf)) {
                    Homepage mf = new Homepage();
                    mf.setVisible(true);
                    mf.pack();
                    mf.setLocationRelativeTo(null);
                    JOptionPane.showMessageDialog(null, "Bem-vindo, Supervisor!");
                    this.dispose();                    
                } else {
                    HomepageA mfa = new HomepageA();
                    mfa.setVisible(true);
                    mfa.pack();
                    mfa.setLocationRelativeTo(null);
                    JOptionPane.showMessageDialog(null, "Bem-vindo, Vendedor!");
                    this.dispose();
                }

            }
            else{
                    JOptionPane.showMessageDialog(null, "CPF e/ou senha inv√°lidos.", "Login falhou!", 2);
                }
            
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

